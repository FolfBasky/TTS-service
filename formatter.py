def prepare_tts_text(text):
    text = text.strip()

    for greek, repl in GREEK_MAP.items():
        text = text.replace(greek, repl)

    for sym, repl in SYMBOL_MAP.items():
        text = text.replace(sym, repl)

    text = re.sub(r'\bM\s*\[', 'математическое ожидание ', text)
    text = re.sub(r'\bD\s*\[', 'дисперсия ', text)
    text = re.sub(r'\bP\s*\(', 'вероятность ', text)

    text = re.sub(r'\bVar\s*\(', 'дисперсия ', text)
    text = re.sub(r'\bCov\s*\(', 'ковариация ', text)
    text = re.sub(r'\bCorr\s*\(', 'корреляция ', text)

    text = re.sub(r'(\w)\^(\w)', r'\1 в степени \2', text)
    text = re.sub(r'(\w)\s*\*\s*(\w)', r'\1 умножить на \2', text)
    text = re.sub(r'(\w)\s*/\s*(\w)', r'\1 разделить на \2', text)

    text = re.sub(r'([\w\)])\s*-\s*(\d)', r'\1 минус \2', text)

    text = re.sub(r'(\w)\s*\(', r'\1 (', text)

    return text


def make_filename(num, title):
    text = f'{num}. {title}'
    for ch in string.punctuation:
        text = text.replace(ch, ' ')
    text = re.sub(r'\s+', '_', text.strip())
    text = text.strip('_')
    return text
